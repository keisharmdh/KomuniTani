package com.example.model;

public class Follower {
    private int id;
    private String name;
    private String email;
    private String bio;

    // Getter untuk id
    public int getId() {
        return id;
    }

    // Setter untuk id
    public void setId(int id) {
        this.id = id;
    }

    // Getter untuk name
    public String getName() {
        return name;
    }

    // Setter untuk name
    public void setName(String name) {
        this.name = name;
    }

    // Getter untuk email
    public String getEmail() {
        return email;
    }

    // Setter untuk email
    public void setEmail(String email) {
        this.email = email;
    }

    // Getter untuk bio
    public String getBio() {
        return bio;
    }

    // Setter untuk bio
    public void setBio(String bio) {
        this.bio = bio;
    }
}